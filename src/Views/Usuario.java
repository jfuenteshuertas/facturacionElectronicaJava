    private void agregarUsuario(){
        if(jtUsuario.getText().length()<=0){
            JOptionPane.showMessageDialog(null,"Debe ingresar un usuario");
            return;
        }
        if(validarContrasenias()){
            guardarUsuario();
            JOptionPane.showMessageDialog(null,"Se agregÃ³ correctamente");
            this.setVisible(false);
        }
    }
