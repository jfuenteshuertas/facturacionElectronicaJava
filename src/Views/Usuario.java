    private Boolean validarContrasenias() {
        String password=String.valueOf(jtPassword.getPassword());
        String repetir=String.valueOf(jtRepetir.getPassword());
        if(password.equals(repetir))return true;
        else {
            JOptionPane.showMessageDialog(null, "Los password  no coinciden");
            return false;
        }
    }

    private void guardarUsuario() {
        PreparedStatement stmt;
        try {
            stmt = conn.prepareStatement("call createAccount(?,?,?,?)");
            stmt.setString(1, jtUsuario.getText());
            String pass=String.valueOf(jtPassword.getPassword());
            stmt.setString(2, pass);
            stmt.setString(3, jtNombres.getText());
            stmt.setInt(4, empresa.getIdEmpresa());
            stmt.executeUpdate();
        } catch (SQLException ex) {
            Logger.getLogger(Usuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void inciarEventosTeclado() {
        //Agrega un  nuevo producto si se presiona la tecla '+' 
        getRootPane().getInputMap(javax.swing.JComponent.WHEN_IN_FOCUSED_WINDOW).put(
            javax.swing.KeyStroke.getKeyStroke(java.awt.event.KeyEvent.VK_ESCAPE, 0), "Cancel");
        
        getRootPane().getActionMap().put("Cancel", new javax.swing.AbstractAction(){
            @Override
            public void actionPerformed(java.awt.event.ActionEvent e)
            {
               setVisible(false);
            }
        });
        getRootPane().getInputMap(javax.swing.JComponent.WHEN_IN_FOCUSED_WINDOW).put(
            javax.swing.KeyStroke.getKeyStroke(java.awt.event.KeyEvent.VK_ENTER, 0), "Add");
        
        getRootPane().getActionMap().put("Add", new javax.swing.AbstractAction(){
            @Override
            public void actionPerformed(java.awt.event.ActionEvent e)
            {
               agregarUsuario();
            }
        });
    }
