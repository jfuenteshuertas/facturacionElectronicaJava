    private boolean tipoCambioSunat() {
        try {
            String tempMes;
            Calendar now =Calendar.getInstance();   
            if((now.get(Calendar.MONTH)+1)<10){
                tempMes="0"+String.valueOf((now.get(Calendar.MONTH)+1));
            }
            else{
                tempMes=String.valueOf((now.get(Calendar.MONTH)+1));
            }
            URL url = new  URL("http://www.sunat.gob.pe/cl-at-ittipcam/tcS01Alias?mes="+tempMes+"&anho="+String.valueOf(now.get(Calendar.YEAR)));
            URLConnection con;
            Boolean band=false;
            ArrayList<String> listTipoCambio=new ArrayList<String>();
            try {
                con = url.openConnection(); 
                con.setConnectTimeout(5000);
                con.setReadTimeout(5000);
                InputStream is =con.getInputStream();
                BufferedReader br = new BufferedReader(new InputStreamReader(is));
                String line = null;
                // read each line and write to System.out
                while ((line = br.readLine()) != null) {
                    if(line.equals("        <td width='4%' align='center' class=\"H3\">"))band=true;
                    if(band){
                        if(line.equals(" </table>"))break;
                        if(!line.equals("        <td width='8%' align='center' class=\"tne10\">") && !line.equals("        </td>") && !line.equals("          </tr>") && !line.equals("          <tr>") && !line.equals("        <td width='4%' align='center' class=\"H3\">" ) && !line.contains("<strong>") && !line.equals("          ") && !line.equals("        ") && !line.equals("      			")){
                            String temp=line.replaceAll("\\s","");
                            //System.out.println(temp);
                            listTipoCambio.add(temp);
                        }    
                    }
                }
                //System.out.println(data);
                clearString(listTipoCambio);
                return true;
            } catch (IOException ex) {
                Logger.getLogger(MainView.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "No se establece conexión con SUNAT, se ingresará el tipo de cambio manualmente");
                modificarTipoCambio();
            }
            } catch (MalformedURLException ex) {
                Logger.getLogger(MainView.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "No se establece conexión con SUNAT, se ingresará el tipo de cambio manualmente");
            }
        return false;
    }

