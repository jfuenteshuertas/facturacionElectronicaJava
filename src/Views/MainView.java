    private void iniciarTabInicio() {
        Image image = Toolkit.getDefaultToolkit().getImage(getClass().getResource("/Images/logoKipu.png"));
        BufferedImage originalImage =((ToolkitImage) image).getBufferedImage();
        int type = originalImage.getType() == 0? BufferedImage.TYPE_INT_ARGB : originalImage.getType();
        jlLogo.setIcon(new ImageIcon(this.resizeImage(originalImage, type)));
    }
    private  BufferedImage resizeImage(BufferedImage originalImage, int type){
		int IMG_WIDTH = 500;//jlLogo.getWidth();
                int IMG_HEIGHT =500;//jlLogo.getHeight();
                BufferedImage resizedImage = new BufferedImage(IMG_WIDTH, IMG_HEIGHT, type);
		Graphics2D g = resizedImage.createGraphics();
		g.drawImage(originalImage, 0, 0, IMG_WIDTH, IMG_HEIGHT, null);
		g.dispose();
		return resizedImage;
    }

