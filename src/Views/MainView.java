    private void modificarTipoCambio() {
        JTextField textField1 = new JTextField((COMPRA>0)?String.valueOf(COMPRA):"3.2");
        JTextField textField2 = new JTextField((VENTA>0)?String.valueOf(VENTA):"3.3");
        Object[] inputFields = {"Compra", textField1,
                                "Venta", textField2};
        int option = JOptionPane.showConfirmDialog(this, inputFields, "Tipo de cambio", JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE,new javax.swing.ImageIcon(getClass().getResource("/Images/sunat_logo.png")));
        if(option == JOptionPane.OK_OPTION) {
            COMPRA=Float.parseFloat(textField1.getText());
            VENTA=Float.parseFloat(textField2.getText());
        }
        asginarTipoCambio();
    }

    private void datosEmpresa() {
        PreparedStatement stmt;
        jlNombreEmpresa.setText(empresa.getNombreComercial());
        try {
            stmt = conn.prepareStatement("SELECT * from Cuenta where idCuenta=?");
            stmt.setInt(1, idCuenta);
            ResultSet rs=stmt.executeQuery();
            cuenta=new Cuenta();
            while(rs.next()){
                cuenta.setIdCuenta(rs.getInt("idCuenta"));
                cuenta.setUsuario(rs.getString("usuario"));
                cuenta.setNombrePersona(rs.getString("nombrePersona"));
                cuenta.setEmpresaIdEmpresa(rs.getInt("Empresa_idEmpresa"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(MainView.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        jlUsuario.setText(cuenta.getNombrePersona());
    }

