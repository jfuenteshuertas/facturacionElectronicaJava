    private void agregarTipoCambio() {
        PreparedStatement stmt;
        try {
            stmt = conn.prepareStatement("select * from tipocambiosunat where idTipocambio=curdate()");
            ResultSet rs=stmt.executeQuery();
            if(rs.next()){
                COMPRA=rs.getFloat("compra");
                VENTA=rs.getFloat("venta");
            }
            else{
                if(!tipoCambioSunat()){
                    COMPRA=3.237f;
                    VENTA=3.24f;
                }
                insertTipoCambio();
            }
            asginarTipoCambio();
        } catch (SQLException ex) {
            Logger.getLogger(MainView.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    private void insertTipoCambio() throws SQLException{
        tipoCambioSunat();
        PreparedStatement stmt;
        stmt = conn.prepareStatement("insert into tipocambiosunat (compra,`venta`) values(?,?)");
        stmt.setFloat(1, COMPRA);
        stmt.setFloat(2, VENTA);
        stmt.executeUpdate();
    }

