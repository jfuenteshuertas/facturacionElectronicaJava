    private void modificarCuenta(){
        PreparedStatement stmt;
        try {
            stmt = conn.prepareStatement("update correoempresa set email=? ,passwordEmail=?, host=?,port=?,starttls=? where Empresa_idEmpresa=?"); 
            stmt.setString(1, jtEmail.getText());
            String tempPass=String.valueOf(jtPassword.getPassword());
            stmt.setString(2, tempPass);
            stmt.setString(3, jtHost.getText());
            stmt.setString(4, jtPort.getText());
            stmt.setString(5, String.valueOf(jcbStartTtls.isSelected()));
            stmt.setInt(6, empresa.getIdEmpresa());
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Se modificaron los cambios");
            this.setVisible(false);
        } catch (SQLException ex) {
            Logger.getLogger(ConfigurarCorreoView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void inciarDatosCorreo() {
        PreparedStatement stmt;
        try {
            stmt = conn.prepareStatement("select *,AES_DECRYPT(passwordEmail, 'LCNDUWF172006000')AS pass from correoempresa where Empresa_idEmpresa=?");
            stmt.setInt(1, empresa.getIdEmpresa());
            ResultSet rs=stmt.executeQuery();
            correo=new CorreoEmpresa();
            if(rs.next()){
                correo.setEmail(rs.getString("email"));
                correo.setStarttls(rs.getString("starttls"));
                correo.setHost(rs.getString("host"));
                correo.setPort(rs.getString("port"));
                passwordEmail=rs.getString("pass");
            }
            if(correo.getEmail().length()>0){
                jtEmail.setText(correo.getEmail());
                jtHost.setText(correo.getHost());
                jtPort.setText(correo.getPort());
                jcbStartTtls.setSelected(Boolean.valueOf(correo.getStarttls()));
                jtPassword.setText(passwordEmail);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ConfigurarCorreoView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
